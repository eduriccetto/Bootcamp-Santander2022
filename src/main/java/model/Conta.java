package model;

public class Conta {

    //Atributos
    private Long numero;
    private Double saudo;

    //Getters and Setters
    public Long getNumero() {
        return numero;
    }

    public void setNumero(Long numero) {
        this.numero = numero;
    }

    public Double getSaudo() {
        return saudo;
    }

    public void setSaudo(Double saudo) {
        this.saudo = saudo;
    }
}
